'''upper(), lower():Trả về một chuỗi được viết hoa hoặc viết thường'''
'''Phương thức isupper(), islower()
Trả về true hoặc false nếu một chuỗi (chữ cái) 
có được viết hoa hay viết thường toàn bộ xâu'''
'''isalpha(): trả về True nếu chuỗi chỉ chứa các ký
tự chữ cái và không có ký tự trắng, còn lại trả về False'''
'''isnumeric(), isdecimal(): Trả về True nếu chuỗi chỉ chứa
các ký tự chữ số và không có ký tự trắng, còn lại trả về False'''
'''isalnum(): trả về True nếu chuỗi chỉ chứa các ký tự chữ cái
hoặc chữ số và không có ký tự trắng, còn lại trả về False'''
'''isspace(): trả về True nếu chuỗi chỉ chứa các ký tự trắng, hoặc dấu tab, hoặc
dấu ngắt dòng, còn lại trả về False'''
'''istitle(): trả về True nếu chuỗi chỉ chứa các từ, mỗi từ được viết thường và
viết hoa chữ cái đầu, còn lại trả về False'''